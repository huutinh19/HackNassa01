String check() {
    if (txtTK.getText().equals("")) {
        return "nhap tai khoan";
    }else if (new String(txtMK.getPassword()).equals("")) {
         return "nhap mat khau";
    }
        for (TaiKhoan t : listTK) {
            if (txtTK.getText().equals(t.getTk())) {
                if (new String(txtMK.getPassword()).equals(t.getMk())) {
                    return t.getRl();
                }
            }
        }
        return "sai tai khoan hoac mat khau";
    }
