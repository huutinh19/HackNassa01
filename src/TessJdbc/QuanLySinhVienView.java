    int check() {
        if (txtMa.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong ma");
            return 1;
        } else if (txtTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong ten");
            return 1;
        } else if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong email");
            return 1;
        } else if (!rdNam.isSelected() && !rdNu.isSelected()) {
            JOptionPane.showMessageDialog(this, "khong de trong gioi tinh");
            return 1;
        } else if (txtDia.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong dia chi");
            return 1;
        } else if (strHinh == null) {
            JOptionPane.showMessageDialog(this, "khong de trong anh");
            return 1;
        }
        for (SinhVien sinhVien : list) {
            if (txtMa.getText().equals(sinhVien.getMa())) {
                JOptionPane.showMessageDialog(this, "khong de trung ma");
                return 1;
            }
        }
        return 0;
    }
