    void loadData() {
        model = (DefaultTableModel) tblSinhVien.getModel();
        model.setRowCount(0);
        for (SinhVien s : list) {
            model.addRow(new Object[]{
                s.getMa(), s.getTen(), s.getEmail(), s.getSdt(), s.getGioi(), s.getDia(), s.getHinh()
            });
        }
    }

    SinhVien getForm() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String email = txtEmail.getText();
        String sdt = txtSDT.getText();
        String gioi = "";
        if (rdNam.isSelected()) {
            gioi = "Nam";
        } else if (rdNu.isSelected()) {
            gioi = "Nu";
        }
        String dia = txtDia.getText();
        String hinh = strHinh;

        return new SinhVien(ma, ten, email, sdt, gioi, dia, hinh, img);
    }

