    int checkThem() {
        if (txtMa.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong ma");
            return 0;
        } else if (txtTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong ten");
            return 0;
        } else if (txtGia.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong gia");
            return 0;
        }else if (!txtGia.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(this, "nhap so");
            return 0;
        }
        for (LT lt : sv.getList()) {
            if (lt.getMa().equals(txtMa.getText())) {
                JOptionPane.showMessageDialog(this, "khong trung ma");
                return 0;
            }
        }
        return 1;
    }
