LT getFrom() {

        String ma = txtMa.getText();
        String ten = txtTen.getText();
        Float gia = Float.valueOf(txtGia.getText());
        Boolean th = cboTrangThai.getSelectedItem() == "ac" ? true : false;
        return new LT(ma, ten, gia, th);
    }
 int checkSua(String ma) {
     if(!ma.equals(txtMa.getText())){
         JOptionPane.showMessageDialog(this, "khong sua ma");
         return 0;
     }
        if (txtMa.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong ma");
            return 0;
        } else if (txtTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong ten");
            return 0;
        } else if (txtGia.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong de trong gia");
            return 0;
        }else if (!txtGia.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(this, "nhap so");
            return 0;
        }
        return 1;
    }
